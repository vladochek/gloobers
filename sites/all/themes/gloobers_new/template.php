/**
 * Add body classes if certain regions have content.
 */
function gloobers_new_preprocess_html(&$variables) {
  if (!empty($variables['page']['featured'])) {
    $variables['classes_array'][] = 'featured';
  }

  if (!empty($variables['page']['triptych_first'])
    || !empty($variables['page']['triptych_middle'])
    || !empty($variables['page']['triptych_last'])) {
    $variables['classes_array'][] = 'triptych';
  }

if (!empty($variables['page']['footer_first'])
    || !empty($variables['page']['footer_second'])
    || !empty($variables['page']['footer_third'])
    || !empty($variables['page']['footer_fourth'])
	|| !empty($variables['page']['footer_fifth'])) {
    $variables['classes_array'][] = 'footer-columns';
  }
   $variables['footer_first'] = block_get_blocks_by_region('footer_first');
   $variables['footer_second'] = block_get_blocks_by_region('footer_second');
   $variables['footer_third'] = block_get_blocks_by_region('footer_third');
   $variables['footer_fourth'] = block_get_blocks_by_region('footer_fourth');
   $variables['footer_fifth'] = block_get_blocks_by_region('footer_fifth');
   $variables['footer'] = block_get_blocks_by_region('footer');
  // Add conditional stylesheets for IE
  drupal_add_css(path_to_theme() . '/css/ie.css', array('group' => CSS_THEME, 'browsers' => array('IE' => 'lte IE 7', '!IE' => FALSE), 'preprocess' => FALSE));
  drupal_add_css(path_to_theme() . '/css/ie6.css', array('group' => CSS_THEME, 'browsers' => array('IE' => 'IE 6', '!IE' => FALSE), 'preprocess' => FALSE));
}

